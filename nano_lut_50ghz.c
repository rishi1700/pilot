void nano_apply_channel_from_lut(uint16_t channel)
{
    if (channel < 1 || channel > LUT_NUM_CHANNELS_50GHZ) {
        return; // or set an error flag if you have one
    }
    const ChannelLutEntry *e = &g_lut_50GHz[channel - 1];

    g_v1   = e->v1_v;
    g_v2   = e->v2_v;
    g_v3   = e->v3_v;
    g_gain = e->gain_mA;
    g_soa  = e->soa_mA;
    g_temp = e->temp_C;
    g_mpd  = e->mpd;
    g_wlpd = e->wlpd;
    g_wmpd = e->wmpd;
}
